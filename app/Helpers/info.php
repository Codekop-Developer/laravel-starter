<?php
if(!function_exists('name_app'))
{
    function name_app($name = null, $img = false) 
    {
        if($name !== null)
        {
            if($img == true)
            {
                return '';
            }
            else
            {
                return '<span style="color:#0d6985;">'.$name.'</span>';
            }
        }
        else
        {
            return '<b><span style="color:#4b8370;"><i class="fa fa-code"></i></span>
                    <span style="color:#0d6985;">CODEKOP.</span><span style="color:#4b8370;">COM</span> 
                </b>';
        }
    }
}

